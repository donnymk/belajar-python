# Operator Perbandingan
#
# > lebih dari
# < kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan
# == sama dengan
# != tidak sama dengan

print("Perbandingan angka")
print(1<=1)
print(10 == 10)
print(10 != 9)
value = 1>4
print(f"1>4: {value}")

print("Perbandingan string")
print("donny" == "donny")
print("donny" != "donny")
